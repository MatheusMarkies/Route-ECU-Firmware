void setup() {
	// --- Carregar Configuração ---
	// Monta o cartão SD
	if (f_mount(&SDFatFS, (TCHAR const*) SDPath, 1) == FR_OK) {
		// Tenta abrir o ficheiro de configuração "config.bin"
		if (f_open(&SDFile, "config.bin", FA_READ) == FR_OK) {
			UINT bytesRead;
			// Lê o ficheiro diretamente para a nossa estrutura de configuração global
			f_read(&SDFile, &g_ecuConfig, sizeof(EcuConfig_t), &bytesRead);
			f_close(&SDFile);

			// Validação simples da configuração
			if (bytesRead != sizeof(EcuConfig_t)
					|| g_ecuConfig.config_version != 1) {
				LoadDefaultConfig(); // Carrega os padrões se o ficheiro for inválido
			}
		} else {
			LoadDefaultConfig(); // Carrega os padrões se o ficheiro não existir
		}
	} else {
		LoadDefaultConfig(); // Carrega os padrões se o SD falhar
	}

	// --- Inicializar Módulos de Firmware ---
	EngineSensors_Init();      // Inicia a leitura ADC+DMA dos sensores
	Crank_Init();           // Inicia o timer de captura para o CKP (VR ou Hall)
	Ignition_Init();           // Inicia os timers de PWM para a ignição
	Injector_Init();           // Inicia os timers de PWM para a injeção
	//Comm_Init(&huart1);        // Inicia a comunicação UART com o PC
	EngineControl_Init();      // Inicia o módulo de controlo (cria o semáforo)
}

